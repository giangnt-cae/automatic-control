function y = manipulability_indexs(x)
    a2 = x(1);
    a3 = x(2);
    q2 = x(3);
    q3 = x(4);
    
    q1_dotmax = 2*pi/3;
    q2_dotmax = 2*pi/3;
    q3_dotmax = pi;
    v_max = 1;
    
    lamda1 = (q1_dotmax^2*(a2^2*cos(2*q2) + a3^2*cos(2*q2 - 2*q3) + a2^2 + ...
        a3^2 + 2*a2*a3*cos(2*q2 - q3) + 2*a2*a3*cos(q3)))/(2*v_max^2);

    lamda2 = ((a2^4*q2_dotmax^4 + a3^4*q2_dotmax^4 + a3^4*q3_dotmax^4 + ...
        4*a2^2*a3^2*q2_dotmax^4 + 2*a3^4*q2_dotmax^2*q3_dotmax^2 + ...
        4*a2*a3^3*q2_dotmax^4*cos(q3) + 4*a2^3*a3*q2_dotmax^4*cos(q3) + ...
        2*a2^2*a3^2*q2_dotmax^4*cos(2*q3) + 2*a2^2*a3^2*q2_dotmax^2* ...
        q3_dotmax^2*cos(2*q3) + 4*a2*a3^3*q2_dotmax^2*q3_dotmax^2*cos(q3))^(1/2) ...
        + a2^2*q2_dotmax^2 + a3^2*q2_dotmax^2 + a3^2*q3_dotmax^2 + ...
        2*a2*a3*q2_dotmax^2*cos(q3))/(2*v_max^2);

    lamda3 = (a2^2*q2_dotmax^2 - (a2^4*q2_dotmax^4 + a3^4*q2_dotmax^4 + ...
        a3^4*q3_dotmax^4 + 4*a2^2*a3^2*q2_dotmax^4 + 2*a3^4*q2_dotmax^2*q3_dotmax^2 + ...
        4*a2*a3^3*q2_dotmax^4*cos(q3) + 4*a2^3*a3*q2_dotmax^4*cos(q3) + ...
        2*a2^2*a3^2*q2_dotmax^4*cos(2*q3) + 2*a2^2*a3^2*q2_dotmax^2*q3_dotmax^2*cos(2*q3) ...
        + 4*a2*a3^3*q2_dotmax^2*q3_dotmax^2*cos(q3))^(1/2) + a3^2*q2_dotmax^2 ...
        + a3^2*q3_dotmax^2 + 2*a2*a3*q2_dotmax^2*cos(q3))/(2*v_max^2);
    
    sigma = [sqrt(lamda1) sqrt(lamda2) sqrt(lamda3)];
    
    w(1) = sigma(1)*sigma(2)*sigma(3);
    w(2) = min(sigma)/max(sigma);
    w(3) = min(sigma);
    w(4) = w(1)^(1/3);
    
    y = [sigma w];
end